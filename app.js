    function findCityByInseeOrName(input) {
      const clean = input.trim().toLowerCase();
      for (const k in presets) {
        if (presets[k].insee === clean || presets[k].name.toLowerCase() === clean || (presets[k].zips && presets[k].zips.includes(clean))) {
          return presets[k];
        }
      }
      return null;
    }

    function findClosestCityByCoords(lat, lon) {
      let closest = null;
      let minDistance = Infinity;
      for (const k in presets) {
        const city = presets[k];
        const dist = Math.pow(city.lat - lat, 2) + Math.pow(city.lon - lon, 2);
        if (dist < minDistance) {
          minDistance = dist;
          closest = city;
        }
      }
      return closest;
    }

    function computeGevReturnLevel(mu, sigma, xi, T) {
      const p = 1 - 1 / T;
      if (xi !== 0) {
        return mu + (sigma / xi) * (Math.pow(-Math.log(p), -xi) - 1);
      } else {
        return mu - sigma * Math.log(-Math.log(p));
      }
    }

    function computeGevCdf(x, mu, sigma, xi) {
      const z = (x - mu) / sigma;
      if (xi !== 0) {
        const v = 1 + xi * z;
        if (v <= 0) return xi > 0 ? 0 : 1;
        return Math.exp(-Math.pow(v, -1 / xi));
      } else {
        return Math.exp(-Math.exp(-z));
      }
    }

    function resolveQuery(url) {
      const cleanUrl = url.trim().replace(/^\/?v1\/risk\//, '').replace(/^\//, '');
      const parts = cleanUrl.split('?');
      const path = parts[0];
      const queryStr = parts[1] || '';

      const params = {};
      queryStr.split('&').forEach(pair => {
        const [k, v] = pair.split('=');
        if (k) params[k] = decodeURIComponent(v);
      });

      const returnPeriod = parseFloat(params.return_period) || 10;
      const year = parseInt(params.year) || 2022;

      let city = null;

      if (path.startsWith('commune/')) {
        const insee = path.replace('commune/', '');
        city = findCityByInseeOrName(insee);
      } else if (path.startsWith('coordinate/')) {
        const coords = path.replace('coordinate/', '').split(',');
        const lat = parseFloat(coords[0]);
        const lon = parseFloat(coords[1]);
        if (!isNaN(lat) && !isNaN(lon)) {
          city = findClosestCityByCoords(lat, lon);
        }
      } else {
        city = findCityByInseeOrName(path);
      }

      if (!city) {
        return {
          status: 404,
          data: {
            status: "error",
            error: "NOT_FOUND",
            message: "Commune non trouvee dans la base de demonstration. Specifiez une commune (/commune/{code_insee}) ou des coordonnees (/coordinate/{lat},{lon}).",
            codes_insee_disponibles: Object.values(presets).map(c => c.insee),
            villes_disponibles: Object.values(presets).map(c => c.name)
          }
        };
      }

      const tNormRequested = (year - 1959) / 63;
      const tNorm1985 = (1985 - 1959) / 63;
      const tNorm2022 = 1.0;

      // Interpolate mu & sigma
      const mu1_val = city.mu1 || 0;
      const sigma1_val = city.sigma1 || 0;

      const mu_req = city.mu0 + mu1_val * tNormRequested;
      const sigma_req = city.sigma0 + sigma1_val * tNormRequested;
      
      const mu_1985 = city.mu0 + mu1_val * tNorm1985;
      const sigma_1985 = city.sigma0 + sigma1_val * tNorm1985;

      const mu_2022 = city.mu0 + mu1_val * tNorm2022;
      const sigma_2022 = city.sigma0 + sigma1_val * tNorm2022;

      const rl_req = computeGevReturnLevel(mu_req, sigma_req, city.xi, returnPeriod);
      const rl_1985 = computeGevReturnLevel(mu_1985, sigma_1985, city.xi, returnPeriod);
      const rl_2022 = computeGevReturnLevel(mu_2022, sigma_2022, city.xi, returnPeriod);

      const se_req = sigma_req * (0.06 + 0.04 * Math.log(returnPeriod));
      const ci_lower = Math.max(0, rl_req - 1.96 * se_req);
      const ci_upper = rl_req + 1.96 * se_req;

      const p_exceed_req = 1 - computeGevCdf(rl_1985, mu_req, sigma_req, city.xi);
      let eq_return_period = p_exceed_req > 0 ? 1 / p_exceed_req : Infinity;
      let freq_mult = p_exceed_req * returnPeriod;

      const thresholdProbabilities = city.thresholds.map(thresh => {
        const p_1985 = 1 - computeGevCdf(thresh, mu_1985, sigma_1985, city.xi);
        const p_2022 = 1 - computeGevCdf(thresh, mu_2022, sigma_2022, city.xi);
        const p_req = 1 - computeGevCdf(thresh, mu_req, sigma_req, city.xi);

        return {
          threshold_24h_mm: thresh,
          annual_probability_historical_1985: (p_1985 * 100).toFixed(2) + "%",
          annual_probability_current_2022: (p_2022 * 100).toFixed(2) + "%",
          annual_probability_requested_year: (p_req * 100).toFixed(2) + "%"
        };
      });

      const relative_change = ((rl_req - rl_1985) / rl_1985 * 100);
      const relative_change_str = (relative_change >= 0 ? "+" : "") + relative_change.toFixed(1) + "%";

      return {
        status: 200,
        cityKey: Object.keys(presets).find(k => presets[k].insee === city.insee),
        data: {
          status: "success",
          query: {
            endpoint: "/v1/risk/" + path,
            parameters: {
              requested_year: year,
              return_period_years: returnPeriod,
              confidence_level: "95%"
            }
          },
          commune: {
            code_insee: city.insee,
            nom: city.name,
            region: city.region,
            coordonnees: {
              latitude: city.lat,
              longitude: city.lon
            }
          },
          model_parameters_gev: {
            location_mu_baseline: parseFloat(city.mu0.toFixed(2)),
            location_mu_slope_per_year: parseFloat((mu1_val / 63).toFixed(4)),
            scale_sigma_baseline: parseFloat(city.sigma0.toFixed(2)),
            scale_sigma_slope_per_year: parseFloat((sigma1_val / 63).toFixed(4)),
            shape_xi: parseFloat(city.xi.toFixed(4)),
            non_stationary_time_covariate_t: parseFloat(tNormRequested.toFixed(4)),
            resolved_location_mu: parseFloat(mu_req.toFixed(2)),
            resolved_scale_sigma: parseFloat(sigma_req.toFixed(2))
          },
          climate_risk_evaluation: {
            target_return_level_mm: parseFloat(rl_req.toFixed(1)),
            confidence_interval_95_pct_mm: [parseFloat(ci_lower.toFixed(1)), parseFloat(ci_upper.toFixed(1))],
            historical_level_1985_mm: parseFloat(rl_1985.toFixed(1)),
            current_level_2022_mm: parseFloat(rl_2022.toFixed(1)),
            evolution_relative_vs_1985: relative_change_str,
            frequency_amplification: {
              historical_return_period_years: returnPeriod,
              equivalent_return_period_under_requested_climate_years: isFinite(eq_return_period) ? parseFloat(eq_return_period.toFixed(1)) : "Infinity",
              probability_multiplier_under_requested_climate: parseFloat(freq_mult.toFixed(2)) + "x"
            }
          },
          threshold_exceedance_probabilities: thresholdProbabilities
        }
      };
    }

