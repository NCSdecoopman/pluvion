      const noise = (Math.abs(rawSin * 43758.5453 % 1) * 2 - 1) * 28;
      const distMed = Math.hypot(x - 68, y - 76);
      const medTrend = 65 * Math.exp(-Math.pow(distMed / 14, 2));
      const hourly = Math.round(15 + medTrend + noise);

      return { clim, daily, hourly };
