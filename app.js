    function gevDensity(x, mu, sigma, xi) {
      const z = (x - mu) / sigma;
      if (xi === 0) {
        const tz = Math.exp(-z);
        return (1 / sigma) * tz * Math.exp(-tz);
      } else {
        const val = 1 + xi * z;
        if (val <= 0) return 0;
        const tx = Math.pow(val, -1 / xi);
        return (1 / sigma) * Math.pow(val, -(1 / xi) - 1) * Math.exp(-tx);
      }
    }

    function drawGevCurves(mu1, sig1, mu2, sig2, xi, rl1, rl2, returnPeriod, year) {
      const path1985 = document.getElementById('path-gev-1985');
      const path2022 = document.getElementById('path-gev-2022');
      const line1985 = document.getElementById('line-rl-1985');
      const line2022 = document.getElementById('line-rl-2022');
      const txt1985 = document.getElementById('txt-rl-1985');
      const txt2022 = document.getElementById('txt-rl-2022');

      const xStart = 5;
      const xEnd = 95;
      const yBaseline = 38;
      const yMaxDensity = 0.05;

      const xValMin = Math.max(5, Math.round(mu1 - 2.2 * sig1));
      const xValMax = Math.round(rl2 * 1.4);

      let points1985 = `M ${xStart} ${yBaseline}`;
      let points2022 = `M ${xStart} ${yBaseline}`;

      for (let x = xStart; x <= xEnd; x++) {
        const val = xValMin + ((x - xStart) / (xEnd - xStart)) * (xValMax - xValMin);
        const dens85 = gevDensity(val, mu1, sig1, xi);
        const dens22 = gevDensity(val, mu2, sig2, xi);

        const y85 = yBaseline - (dens85 / yMaxDensity) * (yBaseline - 8);
        const y22 = yBaseline - (dens22 / yMaxDensity) * (yBaseline - 8);

        points1985 += ` L ${x} ${Math.min(yBaseline, y85)}`;
        points2022 += ` L ${x} ${Math.min(yBaseline, y22)}`;
      }

      points1985 += ` L ${xEnd} ${yBaseline} Z`;
      points2022 += ` L ${xEnd} ${yBaseline} Z`;

      path1985.setAttribute('d', points1985);
      path2022.setAttribute('d', points2022);

      const xRl85 = xStart + ((rl1 - xValMin) / (xValMax - xValMin)) * (xEnd - xStart);
      const xRl22 = xStart + ((rl2 - xValMin) / (xValMax - xValMin)) * (xEnd - xStart);

      line1985.setAttribute('x1', xRl85.toFixed(1));
      line1985.setAttribute('x2', xRl85.toFixed(1));
      line2022.setAttribute('x1', xRl22.toFixed(1));
      line2022.setAttribute('x2', xRl22.toFixed(1));

      txt1985.setAttribute('x', (xRl85 - 2).toFixed(1));
      txt2022.setAttribute('x', (xRl22 + 2).toFixed(1));

      txt1985.textContent = `Hist. 1985 (${returnPeriod}a)`;
      txt2022.textContent = `${year <= 2022 ? 'Cible ' : 'Proj. '}${year} (${returnPeriod}a)`;
    }

